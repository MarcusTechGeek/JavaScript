// We are setting a variable 'counter' to start counting from 0.
let counter = 0;

// We are deciding how much to add to the counter each time (the step).
let step = 5;

// We are using a loop that will keep running as long as the counter is less than or equal to 100.
do {
    // We are showing the current value of the counter.
    console.log(counter);

    // We are adding the step value to the counter.
    counter += step;

// We are checking if the counter is still less than or equal to 100.
} while (counter <= 100); 
