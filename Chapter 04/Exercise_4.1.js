// Variable declaration with the value false
const test = false;

// Logging the initial value of the test variable
console.log(test); // Outputs: false

// Checking if test is true and logging a message if true
if (test) {
    console.log("It's True");
}

// Checking if test is false and logging a message if false
if (!test) {
    console.log("False now"); // Outputs: False now
}
